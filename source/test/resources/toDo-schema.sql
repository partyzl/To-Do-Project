drop table if exists `to_do` CASCADE;
drop table if exists `user` CASCADE;

create table to_do (id bigint generated by default as identity, todo_title varchar(255) not null, user_id bigint, primary key (id));
create table user (id bigint generated by default as identity, age integer not null check (age<=120 AND age>=1), name varchar(255) not null, primary key (id));